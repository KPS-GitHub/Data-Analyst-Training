/* Quantitative and Qualitative Variables */ 


-- Quantitative Variables - can be measured numerically

-- -- -- Discrete Variables - quantities that can be counted in whole numbers, have a finite number of values within a specific range - number of students, cars in a parking lot, goals in a game, etc
-- -- -- Continuous Variables - quantities that can be measured on a scale with infinite values within a specific range. They can be measured to any level of precision, depending on the measuring instrument used - height, weight, temperature, etc



-- Qualitative Variables - aka Categorical Variables - cannot be measured numerically

-- -- -- Nominal Variables - categories with no inherent ordering - hair color, types of fruits
-- -- -- Ordinal Variables - categories with a meaningful order but no consistent difference between categories - satisfaction ratings, educational levels


