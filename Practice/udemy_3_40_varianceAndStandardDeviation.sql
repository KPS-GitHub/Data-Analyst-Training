-- 3-40 - Understanding Variance and Standard Deviation

/* Variance */

-- measures how much data points deviate from the mean, expressed in squared units
-- In data science, variance is crucial for understanding data dispersion and is often used in financial analysis to assess risk. High variance indicates greater data spread and potentially higher risk, while low variance suggests data points are clustered closely around the mean.

-- Variance = [(point_1 - mean)^2 + (point_2 - mean)^2 + ... + (point_n - mean)^2] / N-1



/* Standard Deviation */

-- average distance from the mean
-- widely used in A/B testing to understand variation in conversion rates between groups

-- Standard Deviation = sqrt(Variance)

